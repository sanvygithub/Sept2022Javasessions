package javasessions;

public class ForLoopConcept {

	public static void main(String[] args) {
		int a=10;
		for(;a>=0;a--) {
			System.out.println(a);
		}
System.out.println("---------------------------");
		for(int j=1;j<=10;j++) {
			if(j%2==0)
			System.out.println(j);
		}
		for(char p='a';p<='z';p++) {
			System.out.println((int)(p));
		}
System.out.println("-----------------------------------");
		for(char s='A';s<='Z';s++) {
			System.out.println((int)s);
		}

	}

}
