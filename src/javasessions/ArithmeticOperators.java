package javasessions;

public class ArithmeticOperators {

	public static void main(String[] args) {
		int a=15;
		int b=3;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		double d=12.33;
		double e=12.11;
		System.out.println(d+e);
		System.out.println(0/12);
		//System.out.println(12/0); //Arithmetic Exception
		System.out.println(12/4);
		System.out.println(15/2); //quotient
		System.out.println(15%2); //remainder
		//System.out.println(0/0); //Arithmetic Exception
		System.out.println(0.0/12);
		System.out.println(12/0.0);//Infinity
		System.out.println(12/0.0);//Infinity
		System.out.println(0.0/0.0);
		System.out.println(0/0.0);
		System.out.println(b-2);
		System.out.println(b-a);
		System.out.println(21%2);
		System.out.println(100%6);

	}

}
