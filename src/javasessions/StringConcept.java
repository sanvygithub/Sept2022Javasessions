		int b1=200;
		System.out.println(k+s+a1+b1);
		System.out.println(a1+b1+k+s);
		double d1=23.44;
		double d2=14.44;
		System.out.println(d1+k+" "+s+d2);
		System.out.println(k+s+d1+d2+a1+b1+a+b);
		System.out.println(a1+b1+k+s+d1+d2);
